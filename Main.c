void i2c_init(void) {
    // Enable internal pull-up resistors for PC4 (SDA/A4) and PC5 (SCL/A5)
    PORTC |= (1 << PC4) | (1 << PC5);

    // Initialize TWI at 100kHz for stability
    TWSR = 0x00;
    TWBR = 0x48; // 100kHz at 16MHz
    TWCR = (1 << TWEN);
}

void i2c_start(void) {
    TWCR = (1<<TWINT)|(1<<TWSTA)|(1<<TWEN);
    while (!(TWCR & (1<<TWINT)));
}

void i2c_stop(void) {
    TWCR = (1<<TWINT)|(1<<TWSTO)|(1<<TWEN);
}

void i2c_write(uint8_t data) {
    TWDR = data;
    TWCR = (1<<TWINT)|(1<<TWEN);
    while (!(TWCR & (1<<TWINT)));
}

