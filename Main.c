// Clears the entire screen by writing 0x00 to all pixels
void ssd1306_clear(void) {
    ssd1306_cmd(0x21); ssd1306_cmd(0); ssd1306_cmd(127);
    ssd1306_cmd(0x22); ssd1306_cmd(0); ssd1306_cmd(7);
    
    i2c_start();
    i2c_write(SSD1306_ADDR << 1);
    i2c_write(0x40); // data mode
    for (uint16_t i = 0; i < 1024; i++) {
        i2c_write(0x00);
    }
    i2c_stop();
}

// Sets the exact starting position for the text
void ssd1306_set_cursor(uint8_t column, uint8_t page) {
    // Column goes from 0 to 127. Page goes from 0 to 7.
    ssd1306_cmd(0x21); ssd1306_cmd(column); ssd1306_cmd(127);
    ssd1306_cmd(0x22); ssd1306_cmd(page);   ssd1306_cmd(7);
}

// Draws a single character using the font array
void ssd1306_draw_char(char c) {
    // Check if character is within the font array bounds
    if (c < 32 || c > 126) c = 32; // Default to space if unknown character
    
    uint16_t font_index = (c - 32) * 5; // Calculate where the letter starts in the array

    i2c_start();
    i2c_write(SSD1306_ADDR << 1);
    i2c_write(0x40); // Data mode
    
    // Write the 5 columns of pixels for the letter
    for (uint8_t i = 0; i < 5; i++) {
        i2c_write(pgm_read_byte(&font5x8[font_index + i]));
    }
    // Write 1 empty column for spacing between letters
    i2c_write(0x00); 
    i2c_stop();
}

// Draws a full string of text
void ssd1306_draw_string(uint8_t column, uint8_t page, const char *str) {
    ssd1306_set_cursor(column, page);
    while (*str) {
        ssd1306_draw_char(*str++);
    }
}
